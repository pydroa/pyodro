- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier];
        cell.textLabel.textColor = [UIColor blueColor];
    }

    // Configure the cell...
    //[cell.textLabel setText:@"hogehoge"];
    //cell.textLabel.textColor = [UIColor blueColor];
    //cell.textLabel.text = @"hogehoge";
    
    NSUserDefaults *defaults = [NSUserDefaults standardUserDefaults];
    NSMutableArray *mArray = [defaults arrayForKey:@"LendsList"];
    NSArray *phrases = [[mArray objectAtIndex:indexPath.row] componentsSeparatedByString:@"<>"];
    
    cell.textLabel.text = [phrases objectAtIndex:1];
    
    return cell;
}


